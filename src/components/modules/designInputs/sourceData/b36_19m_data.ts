/**
 * ASME B36.19M — Stainless Steel Pipe Dimensions
 * Source: ASME B36.19M Table 1 — publicly available dimensional data.
 *
 * Same OD as B36.10M (interchangeability). Schedule designations 5S/10S/40S/80S.
 * For NPS ≤ 10″ the S-schedule wall thickness equals the equivalent numeric
 * schedule. For NPS ≥ 12″ the S schedules are noticeably thinner because
 * stainless has higher allowable stress per unit weight.
 */

import type { PipeDimension } from "../pipeScheduleEngine";

const STD = "B36.19M";

// Helper to keep entries terse
const r = (
  nps: string,
  od_mm: number,
  od_in: number,
  schedule: string,
  wt_mm: number,
  wt_in: number,
): PipeDimension => {
  const id_mm = +(od_mm - 2 * wt_mm).toFixed(2);
  // approx weight kg/m for steel: pi * (od - wt) * wt * 7.85 / 1000
  const weightPerMeter = +((Math.PI * (od_mm - wt_mm) * wt_mm * 7.85) / 1000).toFixed(2);
  return { nps, od_mm, od_in, schedule, wt_mm, wt_in, id_mm, weightPerMeter, standard: STD };
};

